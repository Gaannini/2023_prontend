console.log(Math.PI); // 3.141592653589793

console.log(10 / 3); // 3.3333333333333335
console.log(Math.round(10 / 3)); // 3

console.log(10 / 4); // 2.5
console.log(Math.round(10 / 4)); // 3

//제곱
console.log(Math.pow(2, 3)); // 8
console.log(Math.pow(2, 8)); // 256
console.log(Math.pow(2, 10)); // 1024

//제곱근
console.log(Math.sqrt(9)); // 3
console.log(Math.sqrt(16)); // 4

//절대값
console.log(Math.abs(-3)); // 3

//버림
console.log(Math.floor(4.01)); // 4
console.log(Math.floor(4.99)); // 4
//올림
console.log(Math.ceil(4.01)); // 5
console.log(Math.ceil(4.99)); // 5

console.log(Math.sin(Math.PI / 2)); // sin 90도, 1
console.log(Math.cos(Math.PI));     // cos 180도, -1

console.log(Math.max(2, 3)); // 3
console.log(Math.max(2, 3, 5)); // 5
console.log(Math.min(2, 3)); // 2
console.log(Math.min(2, 3, 5)); // 2

